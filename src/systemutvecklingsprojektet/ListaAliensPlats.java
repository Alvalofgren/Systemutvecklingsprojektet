    private void fyllILista() 
    {
        try
        {
     
        ListaVäljPlats.removeAllItems();
        ListaVäljPlats.addItem("Välj");

        String query = "SELECT benamning from plats";
        ArrayList<String> resultat = idb.fetchColumn(query);
        
        for(String värde : resultat)
        {
            ListaVäljPlats.addItem(värde);
        }
        }
        
        catch(InfException undantag)
        {
            JOptionPane.showMessageDialog(null, "Error");
        }
    }

